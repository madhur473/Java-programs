
public class Greater {
	
	public void CalcGreater(int a, int b) {
		if(a>b)
		{
			System.out.println("a is greater");
		}
		else
		{
			System.out.println("b is greater");
		}
	}
	
	public void CalcGreater(float a, float b) {
		if(a>b)
		{
			System.out.println("a is greater");
		}
		else
		{
			System.out.println("b is greater");
		}
	}
	
	public void CalcGreater(char a, char b) {
		if(a>b)
		{
			System.out.println("a is greater");
		}
		else
		{
			System.out.println("b is greater");
		}	
	}
}
